# config.py
# ============================================================
# 项目配置文件：集中存放路径、类别、音频参数、训练超参数和运行设备
# ============================================================
#
# 读这个文件时可以把它理解成“项目设置面板”。
# 训练、预测、数据处理都会从这里读取同一套参数，
# 这样改路径或改训练轮数时，不需要到每个脚本里到处找。

# 导入 PyTorch；这里只用它来判断当前电脑能否使用 CUDA GPU。
import torch


# ────────────────────────────────────────────────
# 随机性 / 模型
# ────────────────────────────────────────────────

# RANDOM_SEED 是随机种子；固定它可以让数据划分和随机增强更容易复现。
RANDOM_SEED = 42

# MODEL_ARCH 表示当前默认使用哪种模型结构；cnn_v2 是当前更强的版本。
MODEL_ARCH = "cnn_v2"  # cnn_v1 兼容旧模型，cnn_v2 用于提升当前训练效果。

# DROPOUT 是 Dropout 概率；训练时随机丢掉部分神经元，减少过拟合。
DROPOUT = 0.2


# ────────────────────────────────────────────────
# 路径配置
# ────────────────────────────────────────────────

# DATA_DIR 是原始音频数据所在目录；里面应按类别名分成多个子文件夹。
DATA_DIR = r"D:\昆明城市学院\音频识别\archive\clips_rd"

# MODEL_SAVE_PATH 是训练时保存最佳模型 checkpoint 的文件路径。
MODEL_SAVE_PATH = r"D:\昆明城市学院\音频识别\best_model.pth"


# ────────────────────────────────────────────────
# 类别配置：列表顺序就是模型输出编号顺序，千万不要随便打乱
# ────────────────────────────────────────────────

# CLASSES 保存全部 20 个英文类别名，并且要和数据集文件夹名称一一对应。
CLASSES = [
    # 第 0 到第 4 类：芦荟、汉堡、卷心菜、蜜饯/糖葫芦类、胡萝卜。
    "aloe", "burger", "cabbage", "candied_fruits", "carrots",

    # 第 5 到第 9 类：薯片、巧克力、饮品、薯条、葡萄。
    "chips", "chocolate", "drinks", "fries", "grapes",

    # 第 10 到第 14 类：软糖、冰淇淋、果冻、面条、腌菜/泡菜。
    "gummies", "ice-cream", "jelly", "noodles", "pickles",

    # 第 15 到第 19 类：披萨、排骨、三文鱼、汤类、鸡翅。
    "pizza", "ribs", "salmon", "soup", "wings",
]

# NUM_CLASSES 是类别总数；模型最后一层输出维度必须等于这个数字。
NUM_CLASSES = len(CLASSES)  # 当前为 20 类。


# ────────────────────────────────────────────────
# 音频参数：决定如何把原始声音转换成 Mel 频谱图
# ────────────────────────────────────────────────

# SAMPLE_RATE 是目标采样率，表示每秒保留多少个声音采样点。
SAMPLE_RATE = 22050  # 单位 Hz，22050 表示每秒 22050 个点。

# DURATION 是统一音频时长；太长会截断，太短会在末尾补 0。
DURATION = 3.0  # 单位秒，本项目把每条音频统一成 3 秒。

# N_MELS 是 Mel 频带数量；最终 Mel 频谱图的“高度”就是 128。
N_MELS = 128

# N_FFT 是短时傅里叶变换的窗口点数；影响频率分辨率。
N_FFT = 1024

# HOP_LENGTH 是相邻音频帧之间移动的采样点数；影响时间轴帧数。
HOP_LENGTH = 512

# F_MIN 是 Mel 频谱保留的最低频率，低于它的频率会被忽略。
F_MIN = 20  # 单位 Hz。

# F_MAX 是 Mel 频谱保留的最高频率，高于它的频率会被忽略。
F_MAX = 8000  # 单位 Hz。


# ────────────────────────────────────────────────
# 训练超参数：决定模型怎样学习、学习多久、一次看多少数据
# ────────────────────────────────────────────────

# BATCH_SIZE 是每次喂给模型的样本数量；越大越吃显存，但统计更稳定。
BATCH_SIZE = 128

# MEL_CACHE_DIR 是 Mel 频谱缓存目录；缓存后训练不用每次重新读音频算频谱。
MEL_CACHE_DIR = r"D:\昆明城市学院\音频识别\mel_cache"

# NUM_EPOCHS 是最多训练轮数；一轮 epoch 表示模型完整看一遍训练集。
NUM_EPOCHS = 80

# LEARNING_RATE 是初始学习率；可以理解成模型每次改参数的“步子大小”。
LEARNING_RATE = 8e-4

# WEIGHT_DECAY 是权重衰减；它会抑制参数过大，从而降低过拟合风险。
WEIGHT_DECAY = 1e-4

# TRAIN_RATIO 是训练集比例；0.8 表示 80% 用于训练，20% 用于验证。
TRAIN_RATIO = 0.8

# NUM_WORKERS 是 DataLoader 子进程数；Windows 如果报多进程问题可改为 0。
NUM_WORKERS = 4

# EARLY_STOP_PATIENCE 是早停耐心值；验证准确率连续这么多轮不提升就停止。
EARLY_STOP_PATIENCE = 15

# LABEL_SMOOTHING 是标签平滑系数；让模型不要对单一类别过度自信。
LABEL_SMOOTHING = 0.05


# ────────────────────────────────────────────────
# SpecAugment 数据增强参数：让模型见到更多“变形后的练习题”
# ────────────────────────────────────────────────

# TIME_MASK_PARAM 控制时间轴遮挡宽度；模拟一小段声音信息缺失。
TIME_MASK_PARAM = 20

# FREQ_MASK_PARAM 控制频率轴遮挡宽度；模拟某些频率信息不明显。
FREQ_MASK_PARAM = 10

# MAX_TIME_SHIFT 控制 Mel 频谱在时间轴上最多随机平移多少帧。
MAX_TIME_SHIFT = 8  # 增强咀嚼事件出现在不同时间位置时的鲁棒性。


# ────────────────────────────────────────────────
# 设备配置：自动选择 GPU 或 CPU
# ────────────────────────────────────────────────

# DEVICE 保存训练/预测使用的设备；有 CUDA GPU 就用 cuda，否则退回 cpu。
DEVICE = torch.device("cuda" if torch.cuda.is_available() else "cpu")
